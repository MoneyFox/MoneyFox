    private static readonly IReadOnlyDictionary<int, Currency> currenciesByNumericIsoCode;
    private static readonly IReadOnlyDictionary<string, Currency> currenciesByAlphaIsoCode;

    /// <summary>
    ///     The collection of all currently supported Currencies
    /// </summary>
    static Currencies()
    {
        var tmpCurrenciesByNumericIsoCode = new Dictionary<int, Currency>();
        var tmpCurrenciesByAlphaIsoCode = new Dictionary<string, Currency>(StringComparer.OrdinalIgnoreCase);
        var fields = typeof(Currencies).GetFields();
        foreach (var field in fields)
        {
            if (field.IsStatic && field.GetValue(null) is Currency currency)
            {
                tmpCurrenciesByNumericIsoCode.Add(key: currency.NumericIsoCode, value: currency);
                tmpCurrenciesByAlphaIsoCode.Add(key: currency.AlphaIsoCode, value: currency);
            }
        }

        currenciesByAlphaIsoCode = tmpCurrenciesByAlphaIsoCode;
        currenciesByNumericIsoCode = tmpCurrenciesByNumericIsoCode;
    }

    public static IReadOnlyList<Currency> GetAll()
    {
        return currenciesByAlphaIsoCode.Values.ToList();
    }

    public static Currency Get(string alphaIsoCode)
    {
        return currenciesByAlphaIsoCode[alphaIsoCode];
    }

    public static Currency Get(int numericIsoCode)
    {
        return currenciesByNumericIsoCode[numericIsoCode];
    }

    public static bool Exists(string alphaIsoCode)
    {
        return !string.IsNullOrEmpty(alphaIsoCode) && currenciesByAlphaIsoCode.ContainsKey(alphaIsoCode);
    }
